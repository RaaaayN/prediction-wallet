"""Stable gateway interfaces for the agent layer."""

from __future__ import annotations

from typing import Protocol


class MarketDataGateway(Protocol):
    def fetch_and_store(self, tickers: list[str], period: str = "1y", force: bool = False) -> dict: ...
    def get_latest_prices(self, tickers: list[str]) -> dict[str, float]: ...
    def get_historical(self, ticker: str, days: int = 90): ...
    def get_refresh_status(self) -> list[dict]: ...


class PortfolioRepository(Protocol):
    def load_portfolio(self) -> dict: ...
    def save_portfolio(self, portfolio: dict) -> None: ...
    def portfolio_snapshot(self, prices: dict[str, float]) -> dict: ...
    def update_peak(self, current_value: float) -> None: ...
    def get_trade_history(self) -> list[dict]: ...


class ExecutionGateway(Protocol):
    def execute_order(self, order: dict, market_price: float, cycle_id: str = "", trades_this_cycle: int = 0): ...


class ResearchGateway(Protocol):
    def summarize(self, tickers: list[str], market_snapshot: dict) -> str: ...


class AuditRepository(Protocol):
    def save_cycle_audit(self, audit: dict) -> int: ...
    def save_decision_trace(self, trace: dict) -> int: ...
